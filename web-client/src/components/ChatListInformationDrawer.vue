  <div>
    <v-card flat color="white">
      <v-card-title>
        <span class="font-weight-bold">
          Room Information
        </span>
        <div class="flex-grow-1"></div>
        <v-btn
          icon
          @click="isRoomInformationFormDialogShow = true"
          v-if="isAccountAdmin"
        >
          <v-icon>mdi-cog</v-icon>
        </v-btn>
      </v-card-title>
      <div class="text-center">
        <v-avatar :size="95">
          <v-img :src="avatarUrl" :lazy-src="avatarUrl"></v-img>
        </v-avatar>
      </div>
      <v-list>
        <v-list-item>
          <v-list-item-content>
            <v-list-item-subtitle>Room Name</v-list-item-subtitle>
            <v-list-item-title class="font-weight-bold">{{
              name
            }}</v-list-item-title>
          </v-list-item-content>
        </v-list-item>
        <v-list-item v-if="description">
          <v-list-item-content>
            <v-list-item-subtitle>Description</v-list-item-subtitle>
            <v-list-item-title>{{ description }}</v-list-item-title>
          </v-list-item-content>
        </v-list-item>
        <v-list-item>
          <v-list-item-content>
            <v-list-item-subtitle>Room Type</v-list-item-subtitle>
            <v-list-item-title>
              <span
                :class="`${
                  type === 'public' ? 'success--text' : 'error--text'
                } text-capitalize`"
              >
                {{ type }}
              </span>
            </v-list-item-title>
          </v-list-item-content>
        </v-list-item>
      </v-list>
      <v-card-title class="font-weight-bold">
        <span>
          Members
        </span>
        <div class="flex-grow-1"></div>
        <v-btn
          color="error"
          icon
          @click="leaveRoom"
          :disabled="!roomId"
          :loading="isLeaveRoomStart"
          v-if="!isAccountAdmin"
        >
          <v-icon>mdi-logout</v-icon>
        </v-btn>
      </v-card-title>
      <v-card-subtitle>{{ membersTitle }} </v-card-subtitle>
      <v-list>
        <template v-for="(member, index) in members">
          <chat-list-information-drawer-member-list-item
            :key="index"
            :admin="admin"
            :room-id="roomId"
            :member-id="member.id"
            :name="member.name"
            :username="member.username"
            :avatar-url="member.avatarUrl"
          ></chat-list-information-drawer-member-list-item>
        </template>
      </v-list>
      <v-card-actions>
